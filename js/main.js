var myRec = new p5.SpeechRec();

function setup() {

    // Background
    createCanvas(800, 400);
    background(color('#000000'));
    fill(255, 255, 255, 255);

    // Instructions
    textSize(32);
    textAlign(CENTER);
    text('Say the name of a color.', width / 2, height / 2);
    myRec.continuous = true; // Do continuous recognition

    // myRec.interimResults = true; // Allow partial recognition (faster, less accurate)
    myRec.onResult = showResult;
    myRec.start();
}

function draw() {}

function showColor(colorName) {
	if (cssColorNames.indexOf(colorName) > -1) {
        background(color(colorName));
	} else {
		background(color('#000000'));
	}
}

function toTitleCase(str) {
    return str.replace(/\w\S*/g, function(txt) {
        return txt.charAt(0).toUpperCase() + txt.substr(1).toLowerCase();
    });
}

function removeSpacesFromString(str){
    return str.replace(/\s/g, '');
}

function showResult() {
    console.log('showResult');
    if (myRec.resultValue === true) {
        var lowerCaseString = myRec.resultString.toLowerCase();
        var titleCaseString = toTitleCase(lowerCaseString);
        var colorString = removeSpacesFromString(titleCaseString);

    	showColor(colorString);
        text(myRec.resultString, width / 2, height / 2);
    }
}
